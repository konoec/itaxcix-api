/**
 * Inicializador espec√≠fico para la p√°gina de Gesti√≥n de Estado de Usuarios
 * Maneja solo los componentes y controladores necesarios para esta p√°gina espec√≠fica
 */
class UserStatusInitializer {
    static async init() {
        console.log('üë• Inicializando p√°gina de Gesti√≥n de Estado de Usuarios...');
        
        if (authChecker.checkAuthentication()) {
            authChecker.updateUserDisplay();
            authChecker.setupLogoutButton();
            
            // Inicializar ComponentLoader
            const componentLoader = new ComponentLoader();
            
            try {
                // Cargar componentes HTML din√°micamente ANTES de inicializar controladores
                console.log('üîÑ Cargando componentes HTML...');
                
                // Cargar sidebar
                await componentLoader.loadComponent('sidebar', '#sidebar-container', {
                    activeSection: window.pageConfig?.activeSection || 'tablas'
                });
                
                // Cargar topbar
                await componentLoader.loadComponent('topbar', '#topbar-container', {
                    pageTitle: window.pageConfig?.pageTitle || { icon: 'fas fa-user-circle', text: 'Gesti√≥n de Estado de Usuarios' }
                });
                
                // Cargar profile modal
                await componentLoader.loadComponent('profile-modal', '#modal-container');
                
                console.log('‚úÖ Todos los componentes HTML cargados');
                
                // Esperar m√°s tiempo para que el DOM se actualice completamente
                setTimeout(() => {
                    // Ahora inicializar controladores que necesitan los elementos del DOM
                    if (!window.sidebarControllerInstance) {
                        window.sidebarControllerInstance = new SidebarController();
                        console.log('üìÅ SidebarController inicializado');
                    }
                    
                    // Inicializar TopBarController DESPU√âS del sidebar con delay adicional
                    setTimeout(() => {
                        if (!window.topBarControllerInstance) {
                            window.topBarControllerInstance = new TopBarController();
                            console.log('üîù TopBarController inicializado');
                        }
                        
                        // Inicializar ProfileController
                        if (!window.profileControllerInstance) {
                            window.profileControllerInstance = new ProfileController();
                            console.log('üë§ ProfileController inicializado');
                            
                            // Establecer referencia al profile controller en topbar
                            if (window.topBarControllerInstance) {
                                window.topBarControllerInstance.profileController = window.profileControllerInstance;
                                console.log('üîó Referencia profile-topbar establecida');
                            }
                        }
                        
                        // Inicializar el controlador de lista de estados de usuario despu√©s de los controladores base
                        setTimeout(() => {
                            try {
                                if (typeof UserStatusListController === 'undefined') {
                                    throw new Error('UserStatusListController no est√° disponible');
                                }
                                window.userStatusListController = new UserStatusListController();
                                console.log('üóÇÔ∏è userStatusListController inicializado por el inicializador');
                            } catch (error) {
                                console.error('‚ùå Error al inicializar UserStatusListController:', error);
                            }
                        }, 200);
                    }, 200);
                    
                    // Inicializar el controlador de creaci√≥n de estado de usuario
                    setTimeout(() => {
                        try {
                            if (typeof CreateUserStatusController === 'undefined') {
                                throw new Error('CreateUserStatusController no est√° disponible');
                            }
                            window.createUserStatusController = new CreateUserStatusController(
                                'createUserStatusModal',
                                'createUserStatusForm',
                                function(newStatus) {
                                    // Refrescar la lista si existe el controlador de lista
                                    if (window.userStatusListController && typeof window.userStatusListController.load === 'function') {
                                        window.userStatusListController.load();
                                    }
                                }
                            );
                            // Asignar evento al bot√≥n + para abrir el modal
                            const btn = document.getElementById('createUserStatusBtn');
                            if (btn) {
                                btn.addEventListener('click', () => window.createUserStatusController.open());
                            }
                            console.log('‚ûï CreateUserStatusController inicializado');
                        } catch (error) {
                            console.error('‚ùå Error al inicializar CreateUserStatusController:', error);
                        }
                    }, 200);

                    // Inicializar el controlador de actualizaci√≥n de estado de usuario
                    setTimeout(() => {
                        try {
                            if (typeof UpdateUserStatusController === 'undefined') {
                                throw new Error('UpdateUserStatusController no est√° disponible');
                            }
                            window.updateUserStatusController = new UpdateUserStatusController(
                                function(updatedStatus) {
                                    // Refrescar la lista si existe el controlador de lista
                                    if (window.userStatusListController && typeof window.userStatusListController.load === 'function') {
                                        window.userStatusListController.load();
                                    }
                                }
                            );
                            console.log('‚úèÔ∏è UpdateUserStatusController inicializado');
                        } catch (error) {
                            console.error('‚ùå Error al inicializar UpdateUserStatusController:', error);
                        }
                    }, 250);
                    
                    // Inicializar el controlador de eliminaci√≥n de estado de usuario
                    setTimeout(() => {
                        try {
                            if (typeof DeleteUserStatusController === 'undefined') {
                                throw new Error('DeleteUserStatusController no est√° disponible');
                            }
                            window.deleteUserStatusController = new DeleteUserStatusController();
                            console.log('üóëÔ∏è DeleteUserStatusController inicializado');
                        } catch (error) {
                            console.error('‚ùå Error al inicializar DeleteUserStatusController:', error);
                        }
                    }, 400);

                    // Inicializar el modal de confirmaci√≥n global
                    setTimeout(() => {
                        try {
                            if (typeof GlobalConfirmationModalController === 'undefined') {
                                console.warn('‚ö†Ô∏è GlobalConfirmationModalController no est√° disponible');
                            } else if (!window.globalConfirmationModalController) {
                                window.globalConfirmationModalController = new GlobalConfirmationModalController();
                                console.log('üóëÔ∏è GlobalConfirmationModalController inicializado');
                            }
                        } catch (error) {
                            console.error('‚ùå Error al inicializar GlobalConfirmationModalController:', error);
                        }
                    }, 150);

                    // Configurar permisos DESPU√âS de que los controladores est√©n listos
                    setTimeout(() => {
                        if (window.PermissionsService) {
                            console.log('üîß Inicializando sistema de permisos...');
                            window.PermissionsService.initializePermissions();
                        }
                        
                        console.log('‚úÖ Estado de Usuarios inicializado completamente');
                        
                        // Notificar que este m√≥dulo ha terminado de cargar
                        LoadingScreenUtil.notifyModuleLoaded('UserStatus');
                    }, 400);
                    
                }, 500);
                
            } catch (error) {
                console.error('‚ùå Error cargando componentes:', error);
                
                // En caso de error, tambi√©n ocultar la pantalla de carga
                LoadingScreenUtil.notifyModuleLoaded('UserStatus');
            }
            
        } else {
            console.log('‚ùå Usuario no autenticado, redirigiendo...');
        }
    }
}

// Auto-inicializar cuando el DOM est√© listo
document.addEventListener('DOMContentLoaded', function() {
    console.log('üìÑ DOM cargado, iniciando UserStatusInitializer...');
    
    // Peque√±o delay para asegurar que todos los scripts est√©n cargados
    setTimeout(() => {
        UserStatusInitializer.init();
    }, 500);
});

console.log('üìù UserStatusInitializer definido y configurado');

